class Solution {
public:
    string smallestString(string s) {
        int n = s.size();
        for (int i = 0; i < n - 1; ++i) {
            if (s[i] <= s[i + 1]) {
                continue;
            }
            return s.substr(0, i) + s.substr(i + 1);
        }
        return s.substr(0, n - 1);
    }
};

